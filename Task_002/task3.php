<?php

$numbers = ["Московская область" => ["Москва", "Зеленоград", "Клин"], "Ленинградская область"=> ["Санкт-Петербург", "Всеволожск", "Павловск", "Кронштадт"], "Новосибирская область"=> ["Новосибирск", "Бердск", "Искитим"]];
foreach($numbers as $key => $value)
{
	$num = count($value);
	echo "$key: ";
	for($i = 0; $i < $num; $i++)
	{
		if($i < $num-1){
			echo "$value[$i], ";
		}
		else{
			echo "$value[$i]; ";
		}
	}
}
